number1 = int(input("Podaj pierwszą liczbe: "))
number2 = int(input("Podaj drubą liczbe: "))


print(f"Suma liczb {number1} i {number2} to: {number1 + number2}")
print(f"Różnica liczby {number1} od {number2} to: {number1 - number2}")
print(f"Iloczyn liczb {number1} i {number2} to: {number1 * number2}")
print(f"Iloraz liczb {number1} i {number2} to: {number1 / number2}")