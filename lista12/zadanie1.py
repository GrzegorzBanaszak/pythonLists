import math


def sum(a, b):
    return a + b


def difference(a, b):
    return a - b


def multiplication(a, b):
    return a * b


def division(a, b):
    return a / b


def square_root(a):
    return math.sqrt(a)


print(sum(2, 3))
print(difference(2, 3))
print(multiplication(2, 3))
print(division(2, 3))
print(square_root(16))
